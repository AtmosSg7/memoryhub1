def _format_money(cents: int, lang: str = "fr") -> str:
    value = (cents or 0) / 100
    if lang == "fr":
        formatted = f"{value:,.2f}".replace(",", "\u00a0").replace(".", ",")
        return f"{formatted} €"
    return f"€{value:,.2f}"


def _format_date(value: Optional[str], lang: str = "fr") -> str:
    if not value:
        return "—"
    try:
        from datetime import datetime

        normalized = str(value).replace("Z", "+00:00")
        dt = datetime.fromisoformat(normalized)
        if lang == "fr":
            return dt.strftime("%d/%m/%Y")
        return dt.strftime("%Y-%m-%d")
    except ValueError:
        return str(value)


def _format_quantity(value) -> str:
    qty = float(value or 1)
    if qty == int(qty):
        return str(int(qty))
    text = f"{qty:.2f}".rstrip("0").rstrip(".")
    return text.replace(".", ",") if "," not in text else text


def _format_discount(value, lang: str) -> str:
    text = str(value or "").strip()
    return text if text else LABELS[lang]["no_discount"]


def _status_label(doc_type: DocumentType, status: str, lang: str) -> str:
    labels = QUOTE_STATUS_LABELS if doc_type == "quote" else INVOICE_STATUS_LABELS
    return labels.get(lang, labels["fr"]).get(status, status)


def _extract_line_items(data: dict) -> list:
    raw_items = data.get("lineItems")
    if not isinstance(raw_items, list):
        return []
    items = []
    for raw in raw_items:
        if not isinstance(raw, dict):
            continue
        description = str(raw.get("description") or "").strip()
        amount_ht = int(raw.get("amountHT") or 0)
        if not description or amount_ht <= 0:
            continue
        items.append(raw)
    return items


def _meta_table_style() -> TableStyle:
    return TableStyle(
        [
            ("FONTNAME", (0, 0), (-1, -1), "Helvetica"),
            ("FONTSIZE", (0, 0), (-1, -1), 10),
            ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#6B7280")),
            ("TEXTCOLOR", (1, 0), (1, -1), colors.HexColor("#111827")),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
            ("TOPPADDING", (0, 0), (-1, -1), 8),
            ("LINEBELOW", (0, 0), (-1, -1), 0.25, colors.HexColor("#E5E7EB")),
        ]
    )


def _build_meta_rows(doc_type: DocumentType, data: dict, labels: dict, lang: str) -> list:
    date_value = data.get("quoteDate") if doc_type == "quote" else data.get("invoiceDate")
    rows = [
        [labels["number"], data.get("number", "—")],
        [labels["status"], _status_label(doc_type, data.get("status", ""), lang)],
        [labels["date"], _format_date(date_value, lang)],
        [labels["client"], data.get("clientName", "—")],
        [labels["title"], data.get("title", "—")],
    ]
    if doc_type == "invoice" and data.get("paidAt"):
        rows.append([labels["paid_at"], _format_date(data.get("paidAt"), lang)])
    return rows


def _build_summary_rows(data: dict, labels: dict, lang: str) -> list:
    vat_rate = data.get("vatRate", 0)
    vat_amount = data.get("amountTTC", 0) - data.get("amountHT", 0)
    return [
        [labels["amount_ht"], _format_money(data.get("amountHT", 0), lang)],
        [labels["vat"], f"{vat_rate} % ({_format_money(vat_amount, lang)})"],
        [labels["amount_ttc"], _format_money(data.get("amountTTC", 0), lang)],
    ]


def _build_line_items_table(line_items: list, labels: dict, lang: str) -> Table:
    header = [
        labels["col_description"],
        labels["col_quantity"],
        labels["col_unit_price"],
        labels["col_vat"],
        labels["col_discount"],
        labels["col_line_total"],
    ]
    rows = [header]
    for line in line_items:
        rows.append(
            [
                line.get("description", ""),
                _format_quantity(line.get("quantity")),
                _format_money(int(line.get("unitPriceHT") or 0), lang),
                f"{int(line.get('vatRate') or 0)} %",
                _format_discount(line.get("discount"), lang),
                _format_money(int(line.get("amountHT") or 0), lang),
            ]
        )

    table = Table(
        rows,
        colWidths=[58 * mm, 14 * mm, 24 * mm, 16 * mm, 20 * mm, 24 * mm],
        repeatRows=1,
    )
    table.setStyle(
        TableStyle(
            [
                ("FONTNAME", (0, 0), (-1, -1), "Helvetica"),
                ("FONTSIZE", (0, 0), (-1, -1), 9),
                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#F3F4F6")),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.HexColor("#374151")),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                ("TEXTCOLOR", (0, 1), (-1, -1), colors.HexColor("#111827")),
                ("ALIGN", (1, 0), (-1, -1), "RIGHT"),
                ("ALIGN", (0, 0), (0, -1), "LEFT"),
                ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
                ("TOPPADDING", (0, 0), (-1, -1), 6),
                ("LEFTPADDING", (0, 0), (-1, -1), 4),
                ("RIGHTPADDING", (0, 0), (-1, -1), 4),
                ("LINEBELOW", (0, 0), (-1, 0), 0.5, colors.HexColor("#D1D5DB")),
                ("LINEBELOW", (0, 1), (-1, -1), 0.25, colors.HexColor("#E5E7EB")),
            ]
        )
    )
    return table


