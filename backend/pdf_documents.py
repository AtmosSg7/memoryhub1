                ("FONTSIZE", (0, 0), (-1, -1), 10),
                ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#6B7280")),
                ("TEXTCOLOR", (1, 0), (1, -2), colors.HexColor("#111827")),
                ("TEXTCOLOR", (0, -1), (-1, -1), colors.HexColor("#0A2540")),
                ("TEXTCOLOR", (1, -1), (1, -1), colors.HexColor("#0A2540")),
                ("ALIGN", (0, 0), (-1, -1), "RIGHT"),
