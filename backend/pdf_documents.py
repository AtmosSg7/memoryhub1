def build_commercial_pdf(doc_type: DocumentType, data: dict, lang: str = "fr") -> bytes:
    lang_key = "fr" if lang == "fr" else "en"
    labels = LABELS[lang_key]
    buffer = BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        leftMargin=20 * mm,
        rightMargin=20 * mm,
        topMargin=20 * mm,
        bottomMargin=20 * mm,
        title=data.get("number") or labels[doc_type],
    )

    styles = getSampleStyleSheet()
    title_style = ParagraphStyle(
        "DocTitle",
        parent=styles["Heading1"],
        fontSize=20,
        textColor=colors.HexColor("#0A2540"),
        spaceAfter=6,
    )
    subtitle_style = ParagraphStyle(
        "DocSubtitle",
        parent=styles["Normal"],
        fontSize=11,
        textColor=colors.HexColor("#4B5563"),
        spaceAfter=14,
    )
    section_style = ParagraphStyle(
        "DocSection",
        parent=styles["Normal"],
        fontSize=11,
        textColor=colors.HexColor("#374151"),
        spaceAfter=6,
        spaceBefore=4,
    )
    notes_style = ParagraphStyle(
        "DocNotes",
        parent=styles["Normal"],
        fontSize=10,
        textColor=colors.HexColor("#374151"),
        leading=14,
    )

    line_items = _extract_line_items(data)
    meta_rows = _build_meta_rows(doc_type, data, labels, lang_key)
    meta_table = Table(meta_rows, colWidths=[45 * mm, 120 * mm])
    meta_table.setStyle(_meta_table_style())

    story = [
        Paragraph(labels[doc_type], title_style),
        Paragraph(data.get("number", ""), subtitle_style),
        meta_table,
    ]

    if line_items:
        story.extend(
            [
                Spacer(1, 10),
                Paragraph(f"<b>{labels['line_items']}</b>", section_style),
                _build_line_items_table(line_items, labels, lang_key),
                Spacer(1, 8),
                _build_totals_table(data, labels, lang_key),
            ]
        )
    else:
        summary_rows = _build_summary_rows(data, labels, lang_key)
        summary_table = Table(summary_rows, colWidths=[45 * mm, 120 * mm])
        summary_table.setStyle(
            TableStyle(
                [
                    ("FONTNAME", (0, 0), (-1, -1), "Helvetica"),
                    ("FONTSIZE", (0, 0), (-1, -1), 10),
                    ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#6B7280")),
                    ("TEXTCOLOR", (1, 0), (1, -1), colors.HexColor("#111827")),
                    ("FONTNAME", (0, -1), (-1, -1), "Helvetica-Bold"),
                    ("TEXTCOLOR", (1, -1), (1, -1), colors.HexColor("#0A2540")),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                    ("TOPPADDING", (0, 0), (-1, -1), 8),
                    ("LINEBELOW", (0, 0), (-1, -2), 0.25, colors.HexColor("#E5E7EB")),
                ]
            )
        )
        story.extend([Spacer(1, 4), summary_table])

    notes = (data.get("internalNotes") or "").strip()
    if notes:
        story.extend(
            [
                Spacer(1, 12),
                Paragraph(f"<b>{labels['notes']}</b>", notes_style),
                Spacer(1, 4),
                Paragraph(notes.replace("\n", "<br/>"), notes_style),
            ]
        )

    story.extend(
        [
            Spacer(1, 24),
            Paragraph(labels["generated"], subtitle_style),
        ]
    )

    doc.build(story)
    return buffer.getvalue()


def build_quote_pdf(quote: dict, lang: str = "fr") -> bytes:
    return build_commercial_pdf("quote", quote, lang)


def build_invoice_pdf(invoice: dict, lang: str = "fr") -> bytes:
    return build_commercial_pdf("invoice", invoice, lang)
